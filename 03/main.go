package main

import "fmt"

func main() {
	fmt.Println("String : ")

	// string
	var name string = "Diptesh"
	var surname = "Deore"
	fmt.Println(name + " " + surname)

	fmt.Println("Int : ")

	// int
	var rollnumber int = 14
	var prn = 121
	fmt.Println(rollnumber)
	fmt.Println(prn)

	fmt.Println("Float : ")

	// float
	var marks float64 = 14.04
	fmt.Println(marks)

	// Simple way
	person := "Diptesh Deore"
	fmt.Println((person))
}
